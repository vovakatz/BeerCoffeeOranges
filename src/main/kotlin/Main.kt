        var data = Collections.synchronizedList(mutableListOf<JSONObject>())

        @JvmStatic fun main(args: Array<String>) {
            for (url in getSensorUrls()) {
                increment()
                while (threadCount > 20){
                    Thread.sleep(100)
                }
                thread (start = true) {
                    try {
                        getData(url)
                    } finally {
                        decrement()
                    }
                }
            }
